package Basicas;

public class Calculadora {

    public void suma(int a, int b) {
        return a + b;
    }

    public void resta(int a, int b) {
        return a - b;
    }

    public int multiplicacion(int a, int b) {
        return a * b;
    }

    public int division(int a, int b) {
        return a / b;
    }

}
